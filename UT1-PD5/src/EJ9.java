public class EJ9 {
    public static void main(String[] args) {
        String hannah = "Did Hannah see bees? Hannah did.";
        System.out.println(hannah.length()); // Retorna un entero con la longitud del String. En este caso es 32
        System.out.println(hannah.charAt(12)); // Retorna un carácter correspondiente con el índice que fue pasado como parámetro. En este caso es e
        System.out.println(hannah.charAt(15)); // retorna b
    }
}
